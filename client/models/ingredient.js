class Ingredient {
    constructor(
      ingredient_name,
      macronutrients,
      micronutrients,
      serving_size,
      url
    ) {
      this.ingredient_name = ingredient_name;
      this.macronutrients = macronutrients;
      this.micronutrients = micronutrients;
      this.serving_size = serving_size;
      this.url = url;
    }
  }
  
  export default Ingredient;
  